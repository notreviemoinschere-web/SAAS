def get_translation(lang: str, key: str) -> str:
    """Get translation by dot-notation key. e.g. 'common.login'"""
    keys = key.split('.')
    data = TRANSLATIONS.get(lang, TRANSLATIONS['en'])
    for k in keys:
        if isinstance(data, dict):
            data = data.get(k, key)
        else:
            return key
    return data if isinstance(data, str) else key
